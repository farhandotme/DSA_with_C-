#include<iostream>
using namespace std;

// addition subtraction product and division of two numbers======>

main()
{
  int a = 20;
  int b = 30;
  // addition
  int sum = a+b;
  cout<<"The sum of two numbers are : "<<sum;
  // subtraction
  int subtraction = a-b;
  cout<<"\nThe subtraction of two numbers are : "<<subtraction;
  // product
  int product = a*b;
  cout<<"\nThe product of two numbers are : "<<product;
  // division
  int divide = a/b;
  cout<<"\nThe divide of two numbers are : "<<divide;
}

